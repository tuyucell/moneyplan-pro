    switch (month) {
      case 1:
        return tr(monthJan, langCode);
      case 2:
        return tr(monthFeb, langCode);
      case 3:
        return tr(monthMar, langCode);
      case 4:
        return tr(monthApr, langCode);
      case 5:
        return tr(monthMay, langCode);
      case 6:
        return tr(monthJun, langCode);
      case 7:
        return tr(monthJul, langCode);
      case 8:
        return tr(monthAug, langCode);
      case 9:
        return tr(monthSep, langCode);
      case 10:
        return tr(monthOct, langCode);
      case 11:
        return tr(monthNov, langCode);
      case 12:
        return tr(monthDec, langCode);
      default:
        return '';
    }
  }

  static String tr(String key, String langCode) {
    return _localizedValues[langCode]?[key] ??
        _localizedValues['tr']![key] ??
        key;
